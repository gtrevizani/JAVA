package cad00_variaveis_e_constantes.teoria;

public class ATT02_TIPOS_PRIMITIVOS {

    public static void main(String[] args) {
        String variavelString = ""; // o tipo String nao possui um tipo primitivo equivalente

        byte variavelByte = 1; // o tipo byte, assim como os outros primitivos, nao pode
                               // manter um estado de ausencia de valor, e é representado pela
                               // palavra null que significa nulo.

        short variavelShort = 1;

        int variavelInteger = 1; // o tipo normal de int é Integer

        long variavelLong = 1L;

        float variavelFloat = 1.0f;

        double variavelDouble = 1.0;

        char variavelCharacter = 'A'; // o tipo normal de char é Character

        boolean variavelBoolean = true;
    }
}
